def get_next_weekday(today: datetime.date, weekday: int) -> datetime.date:
    """Finds the date of the next weekday (0 = Monday, ..., 6 = Sunday)."""
    days_ahead = weekday - today.weekday()
    if days_ahead < 0:  # Already happened this week
        days_ahead += 7
    return today + datetime.timedelta(days_ahead)

def extract_schedule_events(schedule_text: str) -> list:
    """Extracts the structured JSON block of events from the schedule text."""
    # Find a ```json ... ``` code block
    match = re.search(r"```json\s*(.*?)\s*```", schedule_text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group(1).strip())
        except Exception as e:
            logger.error(f"Error parsing schedule JSON block: {e}")
            
    # Try parsing any JSON array in the text as fallback
    try:
        array_match = re.search(r"\[\s*\{.*\}\s*\]", schedule_text, re.DOTALL)
        if array_match:
            return json.loads(array_match.group(0).strip())
    except Exception as e:
        logger.error(f"Fallback array parse failed: {e}")
        
    return []

def generate_ics_content(events: list) -> str:
    """Generates the content of an .ics calendar file from a list of events."""
    lines = [
        "BEGIN:VCALENDAR",
        "VERSION:2.0",
        "PRODID:-//LifePilot AI//NONSGML Calendar//EN",
        "CALSCALE:GREGORIAN",
        "METHOD:PUBLISH"
    ]
    
    days_map = {
        "monday": 0, "tuesday": 1, "wednesday": 2, "thursday": 3,
        "friday": 4, "saturday": 5, "sunday": 6
    }
    
    today = datetime.date.today()
    
    for ev in events:
        summary = ev.get("summary", "LifePilot Activity")
        day_str = ev.get("day", "").lower().strip()
        start_str = ev.get("start_time", "").strip()
        end_str = ev.get("end_time", "").strip()
        
        if day_str not in days_map or not start_str or not end_str:
            continue
            
        target_day_index = days_map[day_str]
        event_date = get_next_weekday(today, target_day_index)
        
        try:
            start_h, start_m = map(int, start_str.split(":"))
            end_h, end_m = map(int, end_str.split(":"))
            
            start_dt = datetime.datetime.combine(event_date, datetime.time(start_h, start_m))
            end_dt = datetime.datetime.combine(event_date, datetime.time(end_h, end_m))
            
            dtstamp = datetime.datetime.utcnow().strftime("%Y%m%dT%H%M%SZ")
            dtstart = start_dt.strftime("%Y%m%dT%H%M%S")
            dtend = end_dt.strftime("%Y%m%dT%H%M%S")
            
            uid = f"{dtstart}-{summary.replace(' ', '_')}@lifepilot.ai"
            
            lines.extend([
                "BEGIN:VEVENT",
                f"UID:{uid}",
                f"DTSTAMP:{dtstamp}",
                f"DTSTART:{dtstart}",
                f"DTEND:{dtend}",
                f"SUMMARY:{summary}",
                "DESCRIPTION:Generated by LifePilot AI",
                "END:VEVENT"
            ])
        except Exception as e:
            logger.error(f"Error formatting event {ev}: {e}")
            
    lines.append("END:VCALENDAR")
    return "\n".join(lines)


@app.post("/api/generate")
def generate_or_update_plan(req: GenerateRequest):
    """
    Parses compound goals, triggers decomposition/planning/guardianship,
    and handles follow-up modifications with streaming progress updates.
    """
    # Force check for API key (unless using Vertex AI keyless auth)
    use_vertex = os.environ.get("USE_VERTEX", "").lower() == "true"
    if not os.environ.get("GEMINI_API_KEY") and not use_vertex:
        raise HTTPException(
            status_code=500,
            detail="GEMINI_API_KEY environment variable is not set on the server."
        )

    q = queue.Queue()
    cancel_event = threading.Event()

    def worker():
        try:
            supervisor = SupervisorAgent()
            if req.follow_up and os.path.exists(STATE_FILE):
                with open(STATE_FILE, "r") as f:
                    current_state = json.load(f)
                
                logger.info("Processing follow-up plan update...")
                new_state = supervisor.update_plan(req.follow_up, current_state, progress_queue=q, cancel_event=cancel_event)
            else:
                logger.info(f"Generating initial plan for goal: {req.goal}")
                new_state = supervisor.generate_initial_plan(req.goal, progress_queue=q, cancel_event=cancel_event)

            # Save state
            with open(STATE_FILE, "w") as f:
                json.dump(new_state, f, indent=2)

            q.put({"result": new_state})
        except Exception as e:
            logger.error(f"Error in generation worker: {e}")
            q.put({"error": str(e)})

    # Start the worker thread
    t = threading.Thread(target=worker)
    t.start()

    def event_generator():
        try:
            while True:
                try:
                    item = q.get(timeout=0.5)
                    if isinstance(item, dict):
                        # This is either {"result": ...} or {"error": ...}
                        yield json.dumps(item) + "\n"
                        break
                    else:
                        # This is a status string
                        yield json.dumps({"status": item}) + "\n"
                except queue.Empty:
                    if not t.is_alive():
                        yield json.dumps({"error": "Generation worker thread terminated unexpectedly."}) + "\n"
                        break
        except GeneratorExit:
            logger.warning("Client disconnected from /api/generate. Triggering cancellation...")
            cancel_event.set()
            t.join(timeout=1.0)

    return StreamingResponse(event_generator(), media_type="application/x-ndjson")


@app.get("/api/state")
def get_current_state():
    """Fetches the persisted state if it exists."""
    if os.path.exists(STATE_FILE):
        try:
            with open(STATE_FILE, "r") as f:
                return json.load(f)
        except Exception as e:
            logger.error(f"Error reading state file: {e}")
            return {}
    return {}


@app.post("/api/clear")
def clear_state():
    """Clears the persisted state."""
    if os.path.exists(STATE_FILE):
        try:
            os.remove(STATE_FILE)
            return {"message": "State cleared successfully."}
        except Exception as e:
            logger.error(f"Error deleting state file: {e}")
            raise HTTPException(status_code=500, detail="Failed to clear state.")
    return {"message": "State was already empty."}


@app.get("/api/download-ics")
def download_calendar():
    """Generates and downloads the .ics file from the current weekly schedule."""
    if not os.path.exists(STATE_FILE):
        raise HTTPException(status_code=404, detail="No plan generated yet. Please generate a plan first.")
        
    try:
        with open(STATE_FILE, "r") as f:
            state = json.load(f)
            
        schedule_text = state.get("current_plan", {}).get("schedule", "")
        if not schedule_text:
            raise HTTPException(status_code=404, detail="No weekly schedule found in the active plan.")
            
        events = extract_schedule_events(schedule_text)
        if not events:
            raise HTTPException(
                status_code=400,
                detail="Could not extract calendar events from the weekly schedule. Ensure the schedule has a valid JSON block."
            )
            
        ics_text = generate_ics_content(events)
        
        return Response(
            content=ics_text,
            media_type="text/calendar",
            headers={
                "Content-Disposition": "attachment; filename=lifepilot_schedule.ics"
            }
        )
    except Exception as e:
        logger.error(f"Error building ICS file: {e}")
        raise HTTPException(status_code=500, detail=f"Failed to compile ICS file: {str(e)}")


